type I18nContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const pathname = usePathname();
  const [language, setLanguageState] = useState<Language>("en");
  const isAppRoute = isLanguageScopedAppRoute(pathname);

  useEffect(() => {
    const syncLanguage = () => {
      if (!isLanguageScopedAppRoute(window.location.pathname)) {
        document.documentElement.lang = "en";
        setLanguageState("en");
        return;
      }

      const stored = localStorage.getItem(languageStorageKey());
      const next = stored === "en" || stored === "vi" ? stored : "en";
      document.documentElement.lang = next;
      setLanguageState(next);
    };

    syncLanguage();
    window.addEventListener("gymfixer-auth-change", syncLanguage);
    return () => window.removeEventListener("gymfixer-auth-change", syncLanguage);
  }, [pathname]);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const setLanguage = useCallback((next: Language) => {
    if (!isAppRoute) {
      setLanguageState("en");
      document.documentElement.lang = "en";
      return;
    }

    setLanguageState(next);
    if (typeof window !== "undefined") {
      localStorage.setItem(languageStorageKey(), next);
      document.documentElement.lang = next;
    }
  }, [isAppRoute]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key: string) => dictionary[key]?.[language] || key,
    }),
    [language, setLanguage]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

function isLanguageScopedAppRoute(pathname: string | null) {
  const path = pathname || "/";
  return path.startsWith("/dashboard") || path.startsWith("/onboarding") || path.startsWith("/payment");
}

function languageStorageKey() {
  const user = getStoredUser();
  const owner = user?.id || user?.email || "anonymous";
  return `${LANGUAGE_KEY_PREFIX}:${owner}`;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside LanguageProvider");
  }
  return context;
}

export function tierLabel(tier: string, t: (key: string) => string) {
  if (tier === "trial") return t("common.trial");
  if (tier === "paid") return t("common.paid");
  return t("common.free");
}

export function translateKey(
  key: string,
  t: (key: string) => string,
  fallback: string,
) {
  const translated = t(key);
  return translated === key ? fallback : translated;
}

export function formatExerciseName(exercise: string, t: (key: string) => string) {
  const fallback = exercise
    .replaceAll("_", " ")
    .replace(/\b\w/g, (char) => char.toUpperCase());
  return translateKey(`exercise.${exercise}`, t, fallback);
}

export function formatStatus(status: string, t: (key: string) => string) {
  const fallback = status
    .replaceAll("_", " ")
    .replace(/\b\w/g, (char) => char.toUpperCase());
  return translateKey(`common.status.${status}`, t, fallback);
}

export function localeFor(t: (key: string) => string) {
  const locale = t("common.locale");
  return locale === "common.locale" ? "en-US" : locale;
}
